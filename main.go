const charset = "abcdefghijklmnopqrstuvwxyz" +
	"ABCDEFGHIJKLMNOPQRSTUVWXYZ0123456789"

// StringWithCharset creates a random string with the charset
// that contains all letters and digits
func StringWithCharset(length int) string {
	b := make([]byte, length)
	for i := range b {
		b[i] = charset[rand.Intn(len(charset))]
	}
	return string(b)
}

// TempFile creates a new temporary file in the directory dir,
// opens the file for reading and writing, and returns the resulting *os.File.
// The filename is generated by taking random string with length 3.
// Multiple programs calling TempFile simultaneously
// will not choose the same file. The caller can use f.Name()
// to find the pathname of the file. It is the caller's responsibility
// to remove the file when no longer needed.
func TempFile(dir string) (f *os.File, err error) {
	nconflict := 0
	for i := 0; i < 10000; i++ {
		name := filepath.Join(dir, StringWithCharset(3))
		f, err = os.OpenFile(name, os.O_RDWR|os.O_CREATE|os.O_EXCL, 0600)
		if os.IsExist(err) {
			if nconflict++; nconflict > 10 {
				rand.Seed(time.Now().UTC().UnixNano())
			}
			continue
		}
		break
	}
	return
}

// Help return message about
func Help(w http.ResponseWriter, r *http.Request) {
	http.Redirect(w, r, "https://github.com/waika28/wpaste.cyou", http.StatusSeeOther)
}

// UploadFile to server and return link to it
func UploadFile(w http.ResponseWriter, r *http.Request) {
	if r.ContentLength > 10<<20 {
		fmt.Fprint(w, "File is too large")
		return
	}

	data := r.FormValue("f")
	file := bytes.NewReader([]byte(data))

	tempFile, err := TempFile(FilesDir())
	if err != nil {
		fmt.Print(err)
	}
	defer tempFile.Close()

	fileBytes, err := ioutil.ReadAll(file)
	if err != nil {
		fmt.Print(err)
	}

	tempFile.Write(fileBytes)
	fmt.Fprint(w, filepath.Base(tempFile.Name()))
}

// SendFile return file by it ID
func SendFile(w http.ResponseWriter, r *http.Request) {
	vars := mux.Vars(r)
	file, err := ioutil.ReadFile(FilesDir() + "/" + vars["id"])
	if err != nil {
		fmt.Fprintf(w, "File %v not found", vars["id"])
		return
	}
	fmt.Fprint(w, string(file))
}

// WpasteRouter return server router
func WpasteRouter() *mux.Router {
	Router := mux.NewRouter().StrictSlash(true)

	Router.HandleFunc("/", Help).Methods("GET")
	Router.HandleFunc("/", UploadFile).Methods("POST")

	Router.HandleFunc("/{id}", SendFile)
	return Router
}

// Basedir return root working directory
func Basedir() string {
	basedir, err := filepath.Abs(filepath.Dir(os.Args[0]))
	if err != nil {
		log.Fatal(err)
	}
	return basedir
}

// FilesDir return directory where uploaded files stored
func FilesDir() string {
	return Basedir() + "/files"
}

// Install prepare to start
func Install() {
	os.Mkdir(FilesDir(), 0766)
	rand.Seed(time.Now().UTC().UnixNano())
}

func main() {
	Install()
	http.ListenAndServe(":9990", WpasteRouter())
}
